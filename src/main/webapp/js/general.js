window.onload = function() {
	$('.int-color').on('focusin',function(){
		$(this).css('background-color','#2196F3');
		$(this).css('color','#ffffff');
		
	});

	
		$('.int-color').on('focusout',function(){
			$(this).css('background-color','#ffffff');
			$(this).css('color','black');
		});
	
}
